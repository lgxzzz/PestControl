package com.pest.control.bean;

public class Pest {
        String PEST_ID;
        String PEST_TYPE;
        String PEST_CONTEX;
        String PEST_URL;
        int PEST_PIC_ID;

    public String getPEST_ID() {
        return PEST_ID;
    }

    public void setPEST_ID(String PEST_ID) {
        this.PEST_ID = PEST_ID;
    }

    public String getPEST_TYPE() {
        return PEST_TYPE;
    }

    public void setPEST_TYPE(String PEST_TYPE) {
        this.PEST_TYPE = PEST_TYPE;
    }

    public String getPEST_CONTEX() {
        return PEST_CONTEX;
    }

    public void setPEST_CONTEX(String PEST_CONTEX) {
        this.PEST_CONTEX = PEST_CONTEX;
    }

    public String getPEST_URL() {
        return PEST_URL;
    }

    public void setPEST_URL(String PEST_URL) {
        this.PEST_URL = PEST_URL;
    }

    public int getPEST_PIC_ID() {
        return PEST_PIC_ID;
    }

    public void setPEST_PIC_ID(int PEST_PIC_ID) {
        this.PEST_PIC_ID = PEST_PIC_ID;
    }
}
